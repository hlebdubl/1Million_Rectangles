public class Rectangle {
    private int length;
    private int width;
    private int x;
    private int y;
    private boolean touches = false;



    public Rectangle(int length, int width, int x, int y){
        this.length = length;
        this.width = width;
        this.x = x;
        this.y = y ;





    }


    public boolean isTouches() {
        return touches;
    }
}
